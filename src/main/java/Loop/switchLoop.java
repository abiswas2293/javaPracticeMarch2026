package Loop;

public class switchLoop {
    public static void main(String args[]){

        String s="India";

        switch(s){

            case "India":
                System.out.println("It is india");
                break;


            case "USA":
                System.out.println("It is USA");

            case "Australia":
                System.out.println("It is Australia");

            case "Sweden":
                System.out.println("It is Sweden");
            default:
                System.out.println("No matching country");










        }





    }
}
